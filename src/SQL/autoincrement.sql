CREATE SEQUENCE SeqClub
START WITH 1
MAXVALUE 999999
MINVALUE 1
NOCYCLE
NOCACHE
NOORDER;

CREATE TRIGGER MonTriggClub
BEFORE INSERT
ON CLUB
FOR EACH ROW
BEGIN
SELECT MySeqClub.NEXTVAL
INTO :NEW.NUMERO_CLUB
FROM CLUB;
END;

/

