CREATE SEQUENCE SeqClub
START WITH 1
INCREMENT BY 1;

CREATE TRIGGER MonTriggClub
BEFORE INSERT
ON CLUB
FOR EACH ROW
BEGIN
SELECT MySeqClub.NEXTVAL
INTO :NEW.NUMERO_CLUB
FROM DUAL;
END;

/

